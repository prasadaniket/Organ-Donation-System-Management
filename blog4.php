    <section class="blog1" id="blog1">

        <h1 class="heading">4 Doctors and a <span>‘Yes’</span> to Organ Donation</h1>

        <div class="row">
            <div class="image">
                <img src="img/blogs/Gulati/Gulati.jpg" alt="">
            </div>

            <div class="content">
                <p><b>Dr. Tribhuvan Gulati on his mother’s donation, and why he wants to talk about it now.</b></p>

                <p>I come from a family of Doctors. My father <b>Dr. Inderjit Gulati</b>, my sisters <b>Dr. Rachna and Dr. Ruchi</b> (who lives
                    in Australia) and myself. My mother, <b>Mrs. Sharda Gulati</b> was the odd one out, but she was the pillar of our house
                    as well as our pathology lab which she managed with aplomb. Not an easy task with all her health problems which
                    started as far back as the year 2000.</p>

                <p>It’s strange to describe your own mother to someone, although easy enough. She was a sweet, caring and giving woman.
                    From everyone who crossed her path personally and professionally, they remember one thing – she would always give
                    daan and she would ensure that everyone was looked after. </p>

            </div>
        </div>
        <div class="content">
            <p> Every Diwali our Lab would be painted over two days and nights and even in the dead of night, the night shift of
                painters would get their tea and snacks so they wouldn’t remain hungry. She would take care of everyone. Till today when
                our Lab gets painted, the same team of painters always remembers her generous spirit.</p>
            </p>
        </div>
        <div class="row">
            <div class="content">
                <p>So, when the most unfortunate day of our lives came about on <b>29th November 2014</b>, exactly 4years ago, it was an
                    easy decision for our family on what to do.</p>

                <p>My mom was having a difficult time since the year 2000. She had a unique problem in her eye, specifically polyps in
                    her retina, which left her blind in one eye in 2000. It was so unique that her case led to the naming of the disease.
                    In 2010 and 2011 she had eyes surgeries and lost most of the vision in both eyes. She had high BP and had 4 strokes
                    in 2013 which led to paralysis, on her left side. Even then she was handling the lab!</p>

                <p>Both my sister and I eventually gave up our jobs as my mother was more comfortable with us looking after her. </p>
            </div>

            <div class="image">
                <img src="img/blogs/Gulati/Pic1.jpg" alt="">
            </div>
        </div>

        <div class="content">
            <p>Still, she kept on doing what she had to do. Such was her strength and resilience. Her condition deteriorated on November
                26th, 2014. She vomited right after taking a bite of food, a sign of another stroke. I gave the first aid and then called
                the ambulance and took her to BL Kapoor Hospital. Dr. Prem, Head of Radiology told me that 40% of the brain was covered
                with blood.</p>

            <p>As a Doctor I understood the prognosis. My mother was gone. I sat alone for 10 minutes to try and figure out how and what
                to say to my father and my sister. How would I tell them? Eventually I came out and spoke to them. At this point I felt
                it was worse being a doctor. I knew there was no hope. If you’re not a Doctor you may cling to some hope and have some
                time to come to the realisation that the person you love is no more. In my case I knew right away, and the reality was
                harsh.</p>

            <p>While we were taking in the loss, the Organ Donation Team approached my father. He was already under a lot of stress and
                asked them to talk to me. I spoke to them and they explained the whole process to me. My father told them that if the kids
                were okay, he didn’t have a problem.</p>

            <p>Eventually we discussed it. My sister was numb with grief but to the both of us the answer was obvious. My mother believed
                in Daan, and she would approve. If she were to take this decision, it would be ‘Yes’, and now a part of her would still be
                living in this world. We waited for my sister Ruchi to arrive from Australia. Her answer was an immediate ‘yes’. Having
                lived in Australia for so many years had already sensitised her on the need for everyone to donate their organs. She
                didn’t need any convincing and we were ready to go for it. The only doubt we had was about the condition of the body, and
                if it would look alright after the organ retrieval had taken place. The doctors informed us that there would be a scar, but
                it would not be visible to others at the cremation.</p>
        </div>

        <div class="row">
            <div class="image">
                <img src="img/blogs/Gulati/Pic2.jpg" alt="">
            </div>

            <div class="content">
                <p>We didn’t know who the organ went to, but we did know that there was a 30-year old mother of two kids who had been
                    admitted to BLK Hospital and needed a liver. She had not got the liver from anywhere and had been given 3-4 months
                    to live. Mom’s liver went to her. I went and got my own organ donor card made right after giving my consent to donating
                    my mother’s organs.</p>

                <p>It’s strange that this the first time that I’m discussing this in pubic. A lot of our relatives don’t know that mom had 
                    donated her organs. Those who had seen the scar and asked us about it and we told them. Somehow, we never openly 
                    announced it. But something has changed.</p>
            </div>
        </div>

        <div class="content">
            <p>I want to talk about it now because something very nice and interesting that happened a year back. I was attending a 
                <b>BL Kapoor Hospital</b> meeting as I was a Consultant there. By sheer coincidence I happened to be chatting to a Doctor 
                who as it turns out, had performed the surgery for the lady who had received Mom’s liver. Somehow because of the dates of 
                my Mom’s death and the surgery of his patient, we figured out that it was my mother’s liver. He never told me who it was 
                but did tell me that the lady who received the liver was doing amazingly well and was living her life to the fullest with 
                her family!</p>
        </div>

        <div class="row">
            <div class="content">
                <p>Till then we knew that by donating my mother’s organs we had done a good thing. But to hear how well the recipient was 
                    doing, took it to another level. It was so good to hear. I shared it with my Dad and he was so happy. I really feel 
                    that everyone should know that organ donation for the donor’s family can be a very fulfilling experience once you can 
                    get past the tragedy. I also now feel that the option of meeting the recipients should be given on a case to case basis 
                    if both sides are ok with it. The recipient should get to say thank you, and for the donor family it would such a good 
                    feeling. It should be an option available in case both sides do want to meet.</p>

                <p><b>I hope that this inspires others to think about organ donation. I can say is with all confidence is that we have 
                    no regrets.</b></p>
            </div>
            
            <div class="image">
                <img src="img/blogs/Gulati/Pic3.jpg" alt="">
            </div>
        </div>
    </section>

