                <p><b>Author Post-Transplant</b></p>

                <p><b>Some pointers:</b></p>

                <p>I turned 50 this March, 2018. I am looking at completing 4 years post transplant, which is a landmark in itself.
                    The post-transplant course has its own challenges but it is a wonderful new life. For those of you out there who
                    are dealing with ESRD, here are some pointers that I can think of:</p>

                <p><b>1.</b> Do not lose hope, do not focus on negatives. A positive mindset albeit tough, goes a long way to defer dialysis
                    or transplant.</p>

                <p><b>2.</b> Always count on family and a core group of friends for support in every possible manner.</p>

                <p><b>3.</b> There will be a huge drain on finances, whether it is dialysis or transplant. There are many places where one
                    can get dialysis at discounted rates. Make sure though that there is no compromise on hygiene since the process
                    involves direct contact with blood.</p>

                <p><b>4.</b> Google is to be used only for very basic queries like finding good places for dialysis or transplant, physicians,
                    costs involved etc and not to try and interpret lab reports. One can get very easily convinced of one’s mortality if
                    Google search results are to be guiding factors.</p>
            </div>
        </div>

        <div class="content">

            <p><b>5.</b> Medicines prescribed pre and post transplant generally have much cheaper generic versions and efforts should be
                made to procure the same.</p>

            <p><b>6.</b> Keep exercising as much as physically possible, eat well within the constraints. And it is perfectly okay to cheat on
                the diet on rare occasions, though one must strictly adhere to water restrictions. Of course once dialysis begins, there
                are no food restrictions and one is in fact supposed to eat a high-protein diet to compensate for losses during dialysis.</p>

            <p><b>7.</b> It is completely okay to feel the blues and indulge in self-pity at times. Things have a way of looking up eventually.
                Remember there will be good days and bad days and a few really bad days, and it is at such times that friends and family
                play a crucial role in boosting one’s morale.</p>

            <p><b>8.</b> The four hours of dialysis plus getting oneself hooked to and unhooked from the dializer can seem a very long time but
                this time should ideally be used to introspect, think good thoughts, catch up with family, engage in inane chats on WhatsApp
                groups, listen to music, read books, in short do anything to take one’s mind off innumerable issues that are bound to crowd
                the mind.</p>

            <p><b>9.</b> Always remember that the immediate family goes through lot of stress and worry along with us, and it is a good idea to
                stand by them as they stand by us through our difficult journey.</p>

            <p><b>10.</b> Do your own bit to encourage organ donation. Keep the faith, always.</p>
