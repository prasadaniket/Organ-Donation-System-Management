        <h1 class="heading">Keep the <span>faith…</span> always</h1>

        <div class="row">
            <div class="image">
                <img src="img/blogs/Sunetra/Sunetra.jpg" alt="">
            </div>

            <div class="content">
                <p><b>Sunetra Chatterjee</b></p>
                <p><b>The Diagnosis…</b> December 2012</p>

                <p>The only change I have noted in myself over the past two months has been greatly increased swelling in my
                    legs and feeling slightly out of breath on my morning walks. I ascribe this to a possible heart condition
                    and decide to get a whole-body check at Indraprastha Apollo Hospital, Delhi. The results are shocking. From
                    a regular happy functional person, at 44 years of age I am suddenly now a patient with <b>ESRD (end-stage renal
                        disease).</b></p>

                <p>This is certainly the worst aspect of this disease, the way it silently and insidiously creeps up and has already
                    done irreversible damage by the time one gets to know. It is unbelievable news for us as a family. We consult doctor after doctor and are handed the same diagnosis and prognosis; that it is an irreversible disease
                    and ultimately I will need either a transplant or have to be on dialysis.</p>

            </div>
        </div>
        <div class="content">
            <p>All my reports are scarily haywire. There is not a single number on my lab values that is anywhere near normal. As I
                begin to Google, the scenario seems bleaker by the minute. Am I even going to live with these values, let alone survive
                and thrive? Six years on I know the futility of trying to interpret isolated values. It is the whole picture that
                physicians look at and these have different interpretations depending upon the age, sex, and other health conditions
                of the patient.</p>

            <p>Thankfully for me I am under the care of the most competent and trustworthy nephrologist that I could have hoped for.
                The plan of action is to undergo major lifestyle changes and to try and keep the blood pressure under control
                (completely over the edge in my case) and hope that the creatinine levels stay within manageable limits, with the rider
                that beyond a certain value it can no longer be managed passively. How much time I have depends on how my body responds
                to medicines and lifestyle changes.</p>

            <p>I am handed a very long list of what seem to only be DO NOTs. It is overwhelming to suddenly know that there are very few
                things that I can eat and that there is massive restriction on water intake. In my case, I am allowed only 1 liter of
                fluids (in any form at all) the whole day, which is very daunting in a hot country like ours. From being a hardcore
                non-vegetarian Bengali, I am suddenly looking at an almost vegetarian regime. And then there are things I must do, like
                get regular exercise. Kidney disease leads to a lot of muscle wasting and being active is of paramount importance.</p>

            <p>Even though the news of having ESRD is terrible and can lower the morale of the most optimistic amongst us, I keep
                trying to look at the positives, which are few to say the least. This is not the end of the world, I tell myself.
                A good doctor, a supportive family, an easy blood group, are some of the positives I focus on in my fight against
                <b>ESRD.</b>
            </p>
        </div>
        <div class="row">
            <div class="content">
                <p>My family</p>
                <p><b>Getting a donor </b>March 2014</p>

                <p>In spite of following all of my Do’s and Dont’s, I am unable to go on for more than a couple of years before my
                    creatinine levels breach the danger mark. I am told in no uncertain terms by my doctor that it is time I began
                    seriously looking at prospective donors in the family. This turns out to be the most harrowing chapter in my journey
                    yet and definitely the most challenging.</p>

                <p>The definition of ‘family’ at Apollo is very restrictive and I am left with only three possible donors to choose
                    from: My 18-year-old only child, my husband, and my 70-year-old frail but tough mother. I do not even wish to bring
                    my daughter into consideration and hence I am left with either my husband or my mother.</p>
            </div>

            <div class="image">
                <img src="img/blogs/Sunetra/Pic1.jpg" alt="">
            </div>
        </div>

        <div class="content">
            <p>Meanwhile my physical symptoms have begun to manifest in different ways. I am beginning to experience unexplained rashes
                over my body, urge to scratch, a constant metallic taste in my mouth, marked nausea at times (all due to build-up of
                toxins), terrible weakness (because of low hemoglobin resulting from ESRD), and occasional lower back pain. And then
                there is bloating and swelling that is most apparent in my legs and on my face, a result of fluid accumulation. I also
                realize I am snoring loud enough to bring the roof down, again due to the fluid build-up.</p>

            <p>My doctor decides it is time to begin me on dialysis while we get searching for a donor. I am admitted for getting a
                temporary access made for dialysis. It is assumed either my husband or my mother will be found fit to donate and it won’t
                be a long wait.</p>

            <p>As is usual in India, as a patient I have no idea what I am supposed to be undergoing. Very casually I am wheeled into an
                anteroom right next to the dialysis unit, asked to lie down on a gurney and look left and keep still, and suddenly I feel
                a cut being made on my neck followed by dabbing, insertion of something, tying up loose ends literally, and I am told it
                is done, whatever the ‘it’ is. Only later I find out that a venous catheter has been put in with a part of it left hanging
                out and the whole thing heavily bandaged. It is literally a pain in the neck.</p>
        </div>

        <div class="row">
            <div class="image">
                <img src="img/blogs/Sunetra/Pic2.jpg" alt="">
            </div>

            <div class="content">
                <p>The next day I am taken for my first dialysis session. I am only told that the first one will be for 2 hours and all
                    subsequent ones of 4 hours each. The dialysis unit at Apollo is busy to the extent they have patients hanging onto beds
                    to get their turn. Naturally there is no one who has time to explain what a dialysis involves, like tubes carrying my
                    impure blood to a dialyzer, another set of tubes returning purified blood to me, strengths and concentrations of dialyzing
                    solution, dry weight, etc., nor the possible nausea or the sudden dip in blood pressure at times. All of this I will
                    eventually figure out myself over time.</p>

                <p>There is bad news on the donor front too. Both my husband and mother have failed their tests. I need to look elsewhere
                    and soon.</p>

                <p><b>Other options? </b>April 2014</p>

                <p>As my options of getting a kidney from within the family fizzle out and the financial drain of thrice-weekly dialyses
                    sessions hits home, we get desperate enough to decide to go against all principles that I hold sacrosanct. From being
                    one who used to feel revulsion at the thought of procuring an organ from another human being, a complete stranger,
                    to save one’s life, I am now willing to set those values aside and do exactly that. I figure out a place where buying a kidney is well
                    established and lots of people have returned with successful transplants after having found willing donors. </p>
            </div>
        </div>

        <div class="content">
            <p>But in the course of my journey I’ve come across many patients who’ve become friends. And I hear horror stories from those who’ve
                already been on that route.</p>

            <p>A friend got in touch with one such ‘broker’ through an acquaintance. Once a financial settlement was made and a token
                amount e-transferred to his account, the tout assures my friend that he was in competent hands, that he has done this
                many times already and has a lot of willing donors ready and waiting for my friend.</p>

            <p>After spending nearly a couple of lakhs on the kidney search including getting a prospective donor tested for everything
                imaginable as also paying the tout for pushing papers through government channels to allegedly beat the red tape, it
                finally culminates in the donor being a match with my friend. The cross-match testing is the most crucial of all since
                it is at this point that the donor gets finalized and one can move on to paperwork, which they were warned can be long
                and very tedious.</p>

            <p>At this point though, my friend is dealt a body blow when he was told one day that his prospective donor has suddenly
                ‘died ’. Obviously the broker had gotten hold of some other patient family who are more desperate and hence willing
                to shell out more, and just when he had his hopes up, it is all over. It is not only a big jolt in terms of the money
                and time lost, it is the broken hopes that hurt the most.</p>

            <p>After hearing this story, we decide to abandon any such thoughts and stay in Delhi and speak to my doctor once again for
                any possible way in which my husband can still donate with the slight problem he has with his own kidney.</p>
        </div>

        <div class="row">
            <div class="content">
                <p><b>Despair……and hope</b> June 2014</p>

                <p>My doctor tells us that we should go in for further tests for my husband and since the problem is minor, we are advised
                    to take it further. Sadly and expectedly, we hit a roadblock again at the cross-match testing. I am talking of a time
                    when swapping donor kidneys between families had still not gained much currency. It looks like all doors and windows
                    are closing on us. Since there is not much to be done, we decide to embark on a new donor search in Kolkata again in a
                    couple of months. We are told of various other cities, countries even, from where people are successfully getting
                    kidneys but the logistics are pretty daunting.</p>

                <p>In all of this time it has completely evaded our minds that my name has automatically made its way into the waiting
                    list at Apollo. Even if we did remember, where was the hope of getting a kidney? People just do not consider donating
                    organs. Much before I found myself in this situation, I have always wanted to donate all my usable organs. It always
                    seemed to me criminal to take functional organs to the grave. We resign ourselves to a long wait and endless
                    dialyses sessions in the near future.</p>
            </div>

            <div class="image">
                <img src="img/blogs/Sunetra/Pic3.jpg" alt="">
            </div>
        </div>

        <div class="content">
            <p>Four years out from that day, I still get goose bumps when I think how lucky I got. I have to admit that when I was first
                diagnosed, I did ask <b>‘why me’</b>. However, my outlook underwent sea change when I saw the spectrum of patients around me and
                realized how blessed in so many ways I actually was in spite of <b>ESRD</b>. Also to be confessed here is that I did not once
                think <b>‘why me’</b> when miraculously destiny gave me a shot at transplant, I passed the cross-match test, and finally underwent
                successful surgery. I guess it is human nature after all. The thankfulness came later and each day since surgery <b>I have
                    been immensely thankful for the unknown family that gave me a new lease of life.</b></p>

            <p>I realize that very few people will actually be as lucky as I am mainly because organ donations are so very few and far in between.
                There is such an immense demand-supply gap and patients and families get so desperate that touts and brokers are thriving. A single
                person can touch and transform so many lives if the family decides to donate organs. Even now as I am documenting this, I ask my
                husband if he remembers any formalities he had to do in the process of me receiving a kidney and he tells me nothing stands out.
                Just a slew of signatures and we were all set. The whole process is so easy and so transparent it is beyond belief. Only the
                family of the deceased has to take this call.</p>

            <p>I know each person has his/her take on organ donation and I respect that. My experience has created a body of willing donors
                within our family. I hope to see more and more people join in the noblest act of giving that any human can ever do.</p>

            <p>After hearing this story, we decide to abandon any such thoughts and stay in Delhi and speak to my doctor once again for
                any possible way in which my husband can still donate with the slight problem he has with his own kidney.</p>
        </div>

